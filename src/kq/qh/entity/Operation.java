package kq.qh.entity;

public class Operation {
	private String id;
	private String name;
	private String pid;
	private String bdclx;
	private Integer timelimit;
	private String zdtzm;
	private String dzwtzm;
	
 
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getBdclx() {
		return bdclx;
	}
	public void setBdclx(String bdclx) {
		this.bdclx = bdclx;
	}
	public Integer getTimelimit() {
		return timelimit;
	}
	public void setTimelimit(Integer timelimit) {
		this.timelimit = timelimit;
	}
	public String getZdtzm() {
		return zdtzm;
	}
	public void setZdtzm(String zdtzm) {
		this.zdtzm = zdtzm;
	}
	public String getDzwtzm() {
		return dzwtzm;
	}
	public void setDzwtzm(String dzwtzm) {
		this.dzwtzm = dzwtzm;
	}
 
	
}
