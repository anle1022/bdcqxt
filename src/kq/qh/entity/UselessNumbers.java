package kq.qh.entity;

public class UselessNumbers {
	private String id;
	private String numberTypeId;
	private String uselessNumber;
	private String year;
	private String selfSystemId;
	private String ajbh;
	private String reason;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNumberTypeId() {
		return numberTypeId;
	}
	public void setNumberTypeId(String numberTypeId) {
		this.numberTypeId = numberTypeId;
	}
 
	public String getUselessNumber() {
		return uselessNumber;
	}
	public void setUselessNumber(String uselessNumber) {
		this.uselessNumber = uselessNumber;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSelfSystemId() {
		return selfSystemId;
	}
	public void setSelfSystemId(String selfSystemId) {
		this.selfSystemId = selfSystemId;
	}
	public String getAjbh() {
		return ajbh;
	}
	public void setAjbh(String ajbh) {
		this.ajbh = ajbh;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
}
